<?php

namespace frontend\models\portal;

use frontend\components\behaviors\CommonModelBehavior;
use Yii;

/**
 * This is the model class for table "portal".
 *
 * @property int $id
 * @property string|null $name Название портала
 * @property string|null $url Адрес портала
 * @property string|null $description Описание портала
 * @property int|null $status Статус портала
 * @property int|null $added_by Добавивший пользователь
 * @property string|null $date_added Дата добавления
 * @property string|null $logo_path Логотип портала
 *
 * @property User $addedBy
 * @property PortalAccount[] $portalAccounts
 */
class Portal extends \yii\db\ActiveRecord
{
    /**
     * {@inheritdoc}
     */
    public static function tableName()
    {
        return '{{%portal}}';
    }

    /**
     * Поведения
     * @return array
     */
    public function behaviors()
    {
        $list = parent::behaviors(); // TODO: Change the autogenerated stub
        $list['common'] = [
            'class' => CommonModelBehavior::class,
        ];
        return $list;
    }

    /**
     * {@inheritdoc}
     */
    public function rules()
    {
        return [
            [['url', 'description'], 'string'],
            [['status', 'added_by'], 'default', 'value' => null],
            [['status', 'added_by'], 'integer'],
            [['date_added'], 'safe'],
            [['name'], 'string', 'max' => 256],
            [['logo_path'], 'string', 'max' => 512],
            [['added_by'],
                'exist',
                'skipOnError' => true,
                'targetClass' => User::className(),
                'targetAttribute' => ['added_by' => 'id']
            ],
        ];
    }

    /**
     * {@inheritdoc}
     */
    public function attributeLabels()
    {
        return [
            'id' => 'ID',
            'name' => 'Name',
            'url' => 'Url',
            'description' => 'Description',
            'status' => 'Status',
            'added_by' => 'Added By',
            'date_added' => 'Date Added',
            'logo_path' => 'Logo Path',
        ];
    }

    /**
     * Gets query for [[AddedBy]].
     *
     * @return \yii\db\ActiveQuery|UserQuery
     */
    public function getAddedBy()
    {
        return $this->hasOne(User::className(), ['id' => 'added_by']);
    }

    /**
     * Gets query for [[PortalAccounts]].
     *
     * @return \yii\db\ActiveQuery|PortalAccountQuery
     */
    public function getPortalAccounts()
    {
        return $this->hasMany(PortalAccount::className(), ['portal_id' => 'id']);
    }

    /**
     * {@inheritdoc}
     * @return PortalQuery the active query used by this AR class.
     */
    public static function find()
    {
        return new PortalQuery(get_called_class());
    }
}
